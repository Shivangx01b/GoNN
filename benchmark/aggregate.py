#!/usr/bin/env python3
"""Aggregate benchmark/results/*.json into benchmark/REPORT.md.

Honest cross-framework comparison. Run after all framework benchmarks have
written their results JSON. Regenerable: `python benchmark/aggregate.py`.
"""
import glob
import json
import os

HERE = os.path.dirname(os.path.abspath(__file__))
RESULTS = os.path.join(HERE, "results")

SIZES_MM = [256, 512, 1024, 2048]
SIZES_EW = [1_000_000, 10_000_000]


def load():
    rows = []
    for f in sorted(glob.glob(os.path.join(RESULTS, "*.json"))):
        with open(f) as fh:
            rows.extend(json.load(fh))
    return rows


def key(r):
    return (r["framework"], r["device"], r["dtype"])


def fmt(v, nd=1):
    return "-" if v is None else f"{v:.{nd}f}"


def matmul_table(rows, dtype, metric):
    """metric: 'gflops' or 'ms_per_iter'."""
    combos = sorted({key(r) for r in rows if r["op"] == "matmul" and r["dtype"] == dtype})
    idx = {(key(r), r["size"]): r for r in rows if r["op"] == "matmul" and r["dtype"] == dtype}
    hdr = "| framework / device | " + " | ".join(f"N={n}" for n in SIZES_MM) + " |"
    sep = "|" + "---|" * (len(SIZES_MM) + 1)
    lines = [hdr, sep]
    for c in combos:
        cells = []
        for n in SIZES_MM:
            r = idx.get((c, n))
            cells.append(fmt(r[metric], 1 if metric == "gflops" else 3) if r else "-")
        label = f"{c[0]} ({c[1]})"
        lines.append(f"| {label} | " + " | ".join(cells) + " |")
    return "\n".join(lines)


def ew_table(rows, op, dtype):
    combos = sorted({key(r) for r in rows if r["op"] == op and r["dtype"] == dtype})
    idx = {(key(r), r["size"]): r for r in rows if r["op"] == op and r["dtype"] == dtype}
    hdr = "| framework / device | " + " | ".join(f"M={n:,}" for n in SIZES_EW) + " |"
    sep = "|" + "---|" * (len(SIZES_EW) + 1)
    lines = [hdr, sep]
    for c in combos:
        cells = []
        for n in SIZES_EW:
            r = idx.get((c, n))
            cells.append(fmt(r["ms_per_iter"], 3) if r else "-")
        lines.append(f"| {c[0]} ({c[1]}) | " + " | ".join(cells) + " |")
    return "\n".join(lines)


def attn_table(rows, causal):
    """Attention GFLOP/s by (framework,dtype) across (bh,seq,head_dim) configs."""
    ar = [r for r in rows if r["op"] == "attention" and r["causal"] == causal]
    cfgs = sorted({(r["bh"], r["seq"], r["head_dim"]) for r in ar})
    combos = sorted({(r["framework"], r["dtype"]) for r in ar})
    idx = {((r["framework"], r["dtype"]), (r["bh"], r["seq"], r["head_dim"])): r for r in ar}
    hdr = "| framework (dtype) | " + " | ".join(f"BH{b}/S{s}/d{d}" for b, s, d in cfgs) + " |"
    sep = "|" + "---|" * (len(cfgs) + 1)
    lines = [hdr, sep]
    for c in combos:
        cells = []
        for cfg in cfgs:
            r = idx.get((c, cfg))
            cells.append(fmt(r["gflops"], 1) if r else "-")
        lines.append(f"| {c[0]} ({c[1]}) | " + " | ".join(cells) + " |")
    return "\n".join(lines)


def main():
    rows = load()
    frameworks = sorted({r["framework"] for r in rows})
    out = []
    out.append("# GoNN cross-framework benchmark results\n")
    out.append("> Auto-generated by `benchmark/aggregate.py` from `benchmark/results/*.json`. "
               "Read `benchmark/REPORT.md` (committed) together with `benchmark/BENCH_SPEC.md` "
               "for methodology and caveats.\n")
    out.append(f"Frameworks present: {', '.join(frameworks)}.\n")

    out.append("## matmul - throughput (GFLOP/s, higher is better)\n")
    out.append("### float64\n")
    out.append(matmul_table(rows, "float64", "gflops") + "\n")
    out.append("### float32 (GoNN's tensor type is f64; the GoNN f32 row is its CUDA backend cuBLAS Sgemm path)\n")
    out.append(matmul_table(rows, "float32", "gflops") + "\n")

    out.append("## matmul - latency (ms/iter, lower is better)\n")
    out.append("### float64\n")
    out.append(matmul_table(rows, "float64", "ms_per_iter") + "\n")

    out.append("## elementwise add - latency (ms/iter, lower is better)\n")
    out.append("### float64\n")
    out.append(ew_table(rows, "elementwise_add", "float64") + "\n")

    out.append("## relu - latency (ms/iter, lower is better)\n")
    out.append("### float64\n")
    out.append(ew_table(rows, "relu", "float64") + "\n")

    if any(r["op"] == "attention" for r in rows):
        out.append("## attention - throughput (GFLOP/s, higher is better)\n")
        out.append("GoNN's fused flash-attention kernel (gonn-flash, f64) vs PyTorch SDPA. "
                   "PyTorch has no fp64 flash kernel (uses the math path); its f32 row uses "
                   "FlashAttention and is shown for reference (GoNN's kernel is f64-only).\n")
        out.append("### non-causal\n")
        out.append(attn_table(rows, False) + "\n")
        out.append("### causal (autoregressive / decoder attention)\n")
        out.append(attn_table(rows, True) + "\n")

    path = os.path.join(HERE, "RESULTS.md")
    with open(path, "w") as fh:
        fh.write("\n".join(out))
    print("wrote", path)


if __name__ == "__main__":
    main()
